    private void lgInMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lgInMouseClicked

        String emplo, Password, query1, query2, fname = null, passDb = null;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3325/finalsproject", "root", "");
            Statement statement = connection.createStatement();

            if ("".equals(uName.getText())) {
                JOptionPane.showMessageDialog(null, "Username is empty", "Error", JOptionPane.ERROR_MESSAGE);
            } else if ("".equals(pWord.getText())) {
                JOptionPane.showMessageDialog(null, "Password is require", "Error", JOptionPane.ERROR_MESSAGE);
            } else if (Integer.valueOf(pWord.getText().length()) < 8) {
                JOptionPane.showMessageDialog(null, "Your password needs to be atleast 8 characters", "message", ERROR_MESSAGE);
            } else {
                emplo = uName.getText();
                Password = pWord.getText();

                query1 = "SELECT * FROM `employee` WHERE GivenName = '" + emplo + "'";
                int f1 = 0, f2 = 0;
                ResultSet rs1 = statement.executeQuery(query1);
                while (rs1.next()) {
                    fname = rs1.getString("GivenName");
                    f1 = 1;
                }

                query2 = "SELECT * FROM `employee` WHERE EmployeeID = '" + Password + "'";

                ResultSet rs2 = statement.executeQuery(query2);
                while (rs2.next()) {
                    passDb = rs2.getString("EmployeeID");
                    f2 = 1;
                }

                if (emplo.equals(fname) && Password.equals(passDb)) {
                    BillingSystem1 BilSys = new BillingSystem1();
                    String wc = "Welcome " + emplo + "!!";
                    BilSys.wel.setText(String.valueOf(wc));
                    BilSys.cashier.setText(String.valueOf(emplo));
                    if (emplo.equals("James")) {
                        BilSys.emploImg.setIcon(new javax.swing.ImageIcon("C:\\Users\\James\\Downloads\\JIM.jpg"));
                    } else if (emplo.equals("Edward")) {
                        BilSys.emploImg.setIcon(new javax.swing.ImageIcon("C:\\Users\\James\\Downloads\\ed (1).jpg"));
                    } else if (emplo.equals("Lawrence")) {
                        BilSys.emploImg.setIcon(new javax.swing.ImageIcon("C:\\Users\\James\\Downloads\\law (1).jpg"));
                    } else if (emplo.equals("Bryan")) {
                        BilSys.emploImg.setIcon(new javax.swing.ImageIcon("C:\\Users\\James\\Downloads\\bj (1).jpg"));
                    } else if (emplo.equals("Jhonny")) {
                        BilSys.emploImg.setIcon(new javax.swing.ImageIcon("C:\\Users\\James\\Downloads\\jasper (1).jpg"));
                    } else {
                        System.out.print("Employee Image doesn't found");
                    }
                    BilSys.setVisible(true);
                    BilSys.pack();
                    BilSys.setLocationRelativeTo(null);
                    this.dispose();
                } else if (f1 == 0 && Password.equals(passDb)) {
                    JOptionPane.showMessageDialog(null, "Incorrect username", "message", ERROR_MESSAGE);
                    uName.setText("");
                } else if (emplo.equals(fname) && f2 == 0) {
                    JOptionPane.showMessageDialog(null, "Incorrect password", "message", ERROR_MESSAGE);
                    pWord.setText("");
                } else {
                    JOptionPane.showMessageDialog(null, "Account not found", "message", ERROR_MESSAGE);
                    uName.setText("");
                    pWord.setText("");
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_lgInMouseClicked

