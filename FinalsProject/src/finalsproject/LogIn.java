    public void getEmployeeId(String emName, String emID) {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3325/finalsproject", "root", "");
            Statement statement = connection.createStatement();

            String emplo = "SELECT * FROM `employe` WHERE GivenName = '" + emName + "'";
            statement.execute(emplo);

            ResultSet rsemplo = statement.executeQuery(emplo);

            while (rsemplo.next()) {
                emID = rsemplo.getString("EmployeeID");
                emName = rsemplo.getString("GivenName");
            }
        } catch (SQLException e) {
            e.printStackTrace();
        } catch (Exception e) {
            System.out.println("Error!" + e.getMessage());
        }
    }
